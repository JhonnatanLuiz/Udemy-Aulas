# Operador lógico "not": Inverte o valor de verdade de uma condição. Se a condição for verdadeira, retorna False; se for falsa, retorna True.

print(not True) # Output: False
print(not False) # Output: True 
print(not 0) # Output: True (0 é considerado False em Python)
print(not 1) # Output: False (1 é considerado True em Python)   