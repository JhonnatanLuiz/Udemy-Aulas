"""
O que é docstring?
Docstring é uma string de documentação que descreve o propósito e o funcionamento de um módulo,
Python é uma linguagem de programação de alto nível, amplamente utilizada para desenvolvimento web, 
automação, análise de dados e inteligência artificial.
Tipo de tipagem: Python é uma linguagem de tipagem dinâmica e forte, 
o que significa que você não precisa declarar o tipo de uma variável ao criá-la.
Tipagem dinâmica: O tipo de uma variável é determinado em tempo de execução,
ou seja, você pode atribuir diferentes tipos de valores a uma variável ao longo do tempo.
String: Uma sequência de caracteres, usada para representar texto.
"""
# Aspas simples e duplas podem ser usadas para definir strings em Python.
print('Olá, Mundo!')  # Resultado: Olá, Mundo!

# Aspas duplas também podem ser usadas para definir strings em Python.
print("Olá, Mundo!")  # Resultado: Olá, Mundo!
print(2, "Olá, 'Mundo!'")  # Resultado: 2 Olá, 'Mundo!'

#Escape de aspas simples dentro de aspas simples
print('O que é Python? \'Python\' é uma linguagem de programação.')  # Resultado: O que é Python? 'Python' é uma linguagem de programação.
#Escape de aspas duplas dentro de aspas duplas
print("O que é Python? \"Python\" é uma linguagem de programação.")  # Resultado: O que é Python? "Python" é uma linguagem de programação.
#r ou R: Raw string, não interpreta caracteres especiais
print(r'O que é Python? \'Python\' é uma linguagem de programação.')  # Resultado: O que é Python? \'Python\' é uma linguagem de programação.
