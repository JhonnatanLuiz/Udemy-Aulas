adicao = 10 + 10
print('Adição:', adicao) # Resultado: Adição: 20

subtracao = 10 - 5
print('Subtração:', subtracao) # Resultado: Subtração: 5

multiplicacao = 10 * 10
print('Multiplicação:', multiplicacao) # Resultado: Multiplicação: 100

divisao = 10 / 2.2
print('Divisão:', divisao) # Resultado: Divisão: 4.545454545454546

divisao_inteira = 10 // 2.2
print('Divisão inteira:', divisao_inteira) # Resultado: Divisão inteira: 4.0

exponenciacao = 2 ** 10
print('Exponenciação:', exponenciacao) # Resultado: Exponenciação: 1024

modulo = 25 % 5
print('Módulo:', modulo) # Resultado: Módulo: 0
# Módulo é o resto da divisão inteira entre dois números.