# Operadores de comparação (relacionais):
# Os operadores de comparação são usados para comparar dois valores e retornam um valor booleano (True ou False).
# Eles são úteis para verificar condições em estruturas de controle, como if, while e for.
# OP | SIGNIFICADO | EXEMPLO (True)
# > | maior que | 10 > 5
# >= | maior ou igual a | 10 >= 5
# < | menor que | 5 < 10
# <= | menor ou igual a | 5 <= 10
# == | igual a | 10 == 10
# != | diferente de | 10 != 5

maior = 2 > 3
maior_ou_igual = 2 >= 2
