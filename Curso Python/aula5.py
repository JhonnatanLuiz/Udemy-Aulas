# Tipo de dado bool: Representa valores booleanos (True ou False).
# Ao questionar algo em um problema, o resultado é sempre True ou False.
print(2 == 2)  # Resultado: True (2 é igual a 2)
print(10 ==11)  # Resultado: False (10 não é igual a 11)
print(type(2 == 2))  # Resultado: <class 'bool'> (booleano)